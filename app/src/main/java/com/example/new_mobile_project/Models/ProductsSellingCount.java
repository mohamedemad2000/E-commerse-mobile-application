package com.example.new_mobile_project.Models;

public class ProductsSellingCount {
    String productName;
    int count;

    public ProductsSellingCount(String productName, int count) {
        this.productName = productName;
        this.count = count;
    }

    public String getProductName() {
        return productName;
    }

    public void setProductName(String productName) {
        this.productName = productName;
    }

    public int getCount() {
        return count;
    }

    public void setCount(int count) {
        this.count = count;
    }
}
